'''Користувач вводить із клавіатури певний текст, після чого користувач
вводить список зарезервованих слів. Необхідно знайти в тексті всі зарезервовані слова
і змінити їхній регістр на верхній. Вивести на екран змінений текст.'''

txt = input('\033[33mВведіть текст: ')
word = input('Введіть солова через пробіл які бажаєте виділити в тексті: ').split()
for i in word:
    txt = txt.replace(i, i.upper())
print(f'\033[35m{txt}')