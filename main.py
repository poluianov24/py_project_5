'''Напишіть функцію, яка відображає горизонтальну або вертикальну лінію з деякого символу.
Функція приймає як параметр довжину лінії, напрямок, символ.'''

def Line(x, y, z=0):
    if z == 0:
        print(f'{y}  ' * x)
    elif z == 1:
        for i in range(0, x):
            print(f'{y}')

Line(9, '#')